    public void applyToPlayer(Player player) {

        if (player.hasPermission("owner")) {
            applyOwner(player);
        } else if (player.hasPermission("manager") || player.hasPermission("developer")) {
            applyManager(player);
        } else if (player.hasPermission("platform-admin") || player.hasPermission("senior-admin")) {
            applyOwner(player);
        } else if (player.hasPermission("admin")) {
            applyAdmin(player);
        } else if (player.hasPermission("trial-mod")) {
            trialMod(player);
        } else if (player.hasPermission("mod")) {
            Mod(player);
        } else if (player.hasPermission("senior-mod")) {
            seniorMod(player);
        } else if (player.hasPermission("famous") || player.hasPermission("youtube")) {
            famous(player);
        } else if (player.hasPermission("partner")) {
            partner(player);
        }
    }
